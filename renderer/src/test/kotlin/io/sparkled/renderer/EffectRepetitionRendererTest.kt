    @Test
    fun can_render_3_repetitions_with_spacing() {
        val repetitions = 3
        val spacing = 2

        val effect = Effect(
            endFrame = 2,
            repetitions = repetitions,
            repetitionSpacing = spacing,
            type = FlashEffect.id,
            easing = Easing(LinearEasing.id, 50f, 50f),
            fill = Fill(
                SingleColorFill.id,
                BlendMode.NORMAL,
                mapOf(
                    arg(SingleColorFill.Params.COLOR.name, "#ffffff")
                )
            )
        )

        val frameCount = (effect.endFrame + spacing + 1) * repetitions
        val renderedStagePropData = RenderUtils.render(effect, frameCount, 10)

